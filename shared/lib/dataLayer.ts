/** Get next auto-incremented company ID via Postgres function */
async function getNextCompanyId(): Promise<number> {
  const { data, error } = await requireClient().rpc('get_next_company_id');
  if (error) throw error;
  return data as number;
}

/** Reserve `count` sequential company IDs; returns the first one */
async function getNextCompanyIds(count: number): Promise<number[]> {
  const { data, error } = await requireClient().rpc('get_next_company_ids', { count });
  if (error) throw error;
  const startId = data as number;
  return Array.from({ length: count }, (_, i) => startId + i);
}

