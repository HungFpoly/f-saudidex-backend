/** Store field confidence and source evidence */
function normalizeEvidenceContent(value: unknown): string {
  if (value == null) return '';
  if (typeof value === 'string') return value.toLowerCase();
  try {
    return JSON.stringify(value).toLowerCase();
  } catch {
    return String(value).toLowerCase();
  }
}

function chooseEvidencePage(
  fieldName: string,
  evidence: unknown,
  value: unknown,
  sourcePages: SourcePageEvidenceRef[],
): SourcePageEvidenceRef | null {
  if (sourcePages.length === 0) return null;

  const evidenceText = normalizeEvidenceContent(evidence).trim();
  const valueText = normalizeEvidenceContent(value).trim();

  if (evidenceText) {
    const exactEvidenceMatch = sourcePages.find((page) => {
      const pageText = `${page.html || ''}\n${page.text_content || page.content || ''}`.toLowerCase();
      return pageText.includes(evidenceText);
    });
    if (exactEvidenceMatch) return exactEvidenceMatch;
  }

  if (valueText && valueText.length >= 4) {
    const valueMatch = sourcePages.find((page) => {
      const pageText = `${page.html || ''}\n${page.text_content || page.content || ''}`.toLowerCase();
      return pageText.includes(valueText);
    });
    if (valueMatch) return valueMatch;
  }

  const preferredPageType = (() => {
    if (['email', 'sales_email', 'procurement_email', 'phone', 'whatsapp', 'contact_email', 'full_address'].includes(fieldName)) {
      return 'contact';
    }
    if (['description_en', 'description_ar', 'name_en', 'name_ar'].includes(fieldName)) {
      return 'about';
    }
    if (['products', 'services', 'categories', 'fields'].includes(fieldName)) {
      return 'products';
    }
    return null;
  })();

  if (preferredPageType) {
    const typedPage = sourcePages.find((page) => page.page_type === preferredPageType);
    if (typedPage) return typedPage;
  }

  return sourcePages[0];
}

async function storeFieldEvidence(companyId: string, data: any, sourcePages: SourcePageEvidenceRef[]): Promise<void> {
  const c = requireClient();
  if (!data.field_confidence) return;

  const sanitizedFieldConfidence = validator.sanitizeConfidenceMap(
    data.field_confidence,
    'field_confidence'
  );

  const entries = Object.entries(sanitizedFieldConfidence).map(([field, score]) => {
    // Priority: 1. AI provided snippet, 2. Field value slice, 3. null
    const evidence = (data.field_evidence?.[field]) || (data[field] ? String(data[field]).slice(0, 500) : null);
    const matchedPage = chooseEvidencePage(field, data.field_evidence?.[field], data[field], sourcePages);
    
    return {
      company_id: companyId,
      field_name: field,
      confidence: score,
      source_url: matchedPage?.url || null,
      source_page_id: matchedPage?.id || null,
      extraction_method: 'ai_suggestion',
      extraction_detail: evidence,
      value: data[field] || null,
      is_locked: score >= 0.9
    };
  });

  if (entries.length > 0) {
    const { error } = await c.from('field_evidence').upsert(entries, { onConflict: 'company_id,field_name' });
    if (error) console.warn("[DataLayer] Failed to store field evidence:", error.message);
  }
}

/** Store raw HTML for a company (scraped pages) */
async function storeRawHtml(companyId: string, pages: Array<{ url: string; html: string; page_type: string }>): Promise<void> {
  const c = requireClient();
  if (pages.length === 0) return;

  const payload = {
    company_id: companyId,
    pages: pages.map((p, i) => ({
      ...p,
      scraped_at: new Date().toISOString(),
      order: i
    })),
    stored_at: new Date().toISOString()
  };

  const { error } = await c.from('company_raw_html').upsert(payload, { onConflict: 'company_id' });
  if (error) console.warn("[DataLayer] Failed to store raw HTML:", error.message);
}

function tryGetDomain(url?: string | null): string | null {
  if (!url) return null;
  try {
    return new URL(url).hostname.toLowerCase();
  } catch {
    return null;
  }
}

async function ensureScrapeSource(baseUrl: string, name?: string, type: string = 'website'): Promise<string | null> {
  const c = requireClient();
  const normalizedName = name || tryGetDomain(baseUrl) || baseUrl;

  const { data, error } = await c
    .from('scrape_sources')
    .upsert({
      base_url: baseUrl,
      name: normalizedName,
      type,
      is_active: true,
    }, { onConflict: 'base_url' })
    .select('id')
    .single();

  if (error) {
    console.warn('[DataLayer] Failed to ensure scrape source:', error.message);
    return null;
  }

  return data?.id ?? null;
}

async function createScrapeRun(sourceId: string | null): Promise<string | null> {
  const c = requireClient();
  const { data, error } = await c
    .from('scrape_runs')
    .insert({ source_id: sourceId, status: 'running' })
    .select('id')
    .single();

  if (error) {
    console.warn('[DataLayer] Failed to create scrape run:', error.message);
    return null;
  }

  return data?.id ?? null;
}

async function finalizeScrapeRun(
  runId: string | null,
  updates: {
    status: string;
    total_pages?: number;
    total_success?: number;
    total_failed?: number;
    error_message?: string | null;
  }
): Promise<void> {
  if (!runId) return;
  const c = requireClient();

  const { error } = await c
    .from('scrape_runs')
    .update({
      ...updates,
      finished_at: new Date().toISOString(),
    })
    .eq('id', runId);

  if (error) {
    console.warn('[DataLayer] Failed to finalize scrape run:', error.message);
  }
}

