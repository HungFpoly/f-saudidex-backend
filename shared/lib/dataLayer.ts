async function storeSourcePages(
  pages: ScrapedPageResult[],
  options?: { crawlJobId?: string | null; seedId?: string | null; defaultStatus?: 'raw' | 'parsed' | 'error' }
): Promise<StoredSourcePageRow[]> {
  const c = requireClient();
  if (pages.length === 0) return [];

  const rows = pages.map(page => ({
    url: page.url,
    canonical_url: page.url,
    domain: tryGetDomain(page.url),
    page_type: page.page_type || 'other',
    html: page.html,
    text_content: page.content,
    title: null,
    status: options?.defaultStatus || 'raw',
    crawl_job_id: options?.crawlJobId || null,
    seed_id: options?.seedId || null,
    response_code: page.response_code || null,
    response_headers: page.response_headers || null,
    fetch_method: page.fetch_method || null,
    fetch_time_ms: null,
    html_size_bytes: page.html?.length || 0,
    crawled_at: new Date().toISOString(),
  }));

  const { data, error } = await c
    .from('source_pages')
    .insert(rows)
    .select('id, url, canonical_url');
  if (error) {
    console.warn('[DataLayer] Failed to store source pages:', error.message);
    return [];
  }

  return (data || []) as StoredSourcePageRow[];
}

async function syncCompanyContacts(companyId: string, data: Record<string, unknown>, sourceUrl?: string | null): Promise<void> {
  const c = requireClient();
  const desired = [
    { contact_type: 'general', email: data.email, phone: data.phone, whatsapp: data.whatsapp },
    { contact_type: 'general', email: data.contact_email, phone: null, whatsapp: null },
    { contact_type: 'sales', email: data.sales_email, phone: null, whatsapp: null },
    { contact_type: 'procurement', email: data.procurement_email, phone: null, whatsapp: null },
  ]
    .filter(entry => entry.email || entry.phone || entry.whatsapp)
    .map(entry => ({
      company_id: companyId,
      contact_type: entry.contact_type,
      email: typeof entry.email === 'string' ? entry.email : null,
      phone: typeof entry.phone === 'string' ? entry.phone : null,
      whatsapp: typeof entry.whatsapp === 'string' ? entry.whatsapp : null,
      source_url: sourceUrl || null,
      verified: false,
    }));

  if (desired.length === 0) return;

  const { data: existing, error: fetchError } = await c
    .from('company_contacts')
    .select('contact_type, email, phone, whatsapp')
    .eq('company_id', companyId);

  if (fetchError) {
    console.warn('[DataLayer] Failed to read company contacts:', fetchError.message);
    return;
  }

  const existingKeys = new Set((existing || []).map(row => [
    row.contact_type || '',
    row.email || '',
    row.phone || '',
    row.whatsapp || '',
  ].join('|')));

  const toInsert = desired.filter(row => !existingKeys.has([
    row.contact_type || '',
    row.email || '',
    row.phone || '',
    row.whatsapp || '',
  ].join('|')));

  if (toInsert.length === 0) return;

  const { error } = await c.from('company_contacts').insert(toInsert);
  if (error) {
    console.warn('[DataLayer] Failed to sync company contacts:', error.message);
  }
}

