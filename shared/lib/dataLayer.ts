/** Fetch a single row by id */
async function getDoc<T>(table: string, id: string): Promise<T | null> {
  const { data, error } = await requireClient()
    .from(table)
    .select('*')
    .eq('id', id)
    .maybeSingle();
  if (error) throw error;
  return data as T | null;
}

/** Query rows with optional filters, ordering, and limit */
async function queryDocs<T>(
  table: string,
  filters: Array<{ field: string; op: string; value: unknown }> = [],
  options?: { orderBy?: { field: string; direction?: 'asc' | 'desc' }; limit?: number }
): Promise<T[]> {
  let q = requireClient().from(table).select('*');

  for (const f of filters) {
    const method = OP_MAP[f.op] ?? 'eq';
    q = (q as any)[method](f.field, f.value);
  }

  if (options?.orderBy) {
    q = q.order(options.orderBy.field, { ascending: options.orderBy.direction !== 'desc' });
  }
  if (options?.limit) {
    q = q.limit(options.limit);
  }

  const { data, error } = await q;
  if (error) throw error;
  return (data ?? []) as T[];
}

/** Upsert (insert or update) a row */
async function setDoc(table: string, id: string, data: Record<string, unknown>): Promise<void> {
  const row = table === 'companies' ? sanitizeCompanyPayload({ ...data, id }) : { ...data, id };
  const { error } = await requireClient()
    .from(table)
    .upsert(row, { onConflict: 'id' });
  if (error) throw error;
}

/** Update specific fields on a row */
async function updateDoc(table: string, id: string, data: Record<string, unknown>): Promise<void> {
  const payload = table === 'companies' ? sanitizeCompanyPayload({ ...data }) : data;
  const { error } = await requireClient()
    .from(table)
    .update(payload)
    .eq('id', id);
  if (error) throw error;
}

/** Delete a row */
async function deleteDoc(table: string, id: string): Promise<void> {
  const { error } = await requireClient().from(table).delete().eq('id', id);
  if (error) throw error;
}

/** Insert a new row (no id required — auto-generated by DB if not supplied) */
async function addDoc(table: string, data: Record<string, unknown>): Promise<{ id: string }> {
  const payload = table === 'companies' ? sanitizeCompanyPayload({ ...data }) : data;
  const { data: inserted, error } = await requireClient()
    .from(table)
    .insert(payload)
    .select('id')
    .single();
  if (error) throw error;
  return inserted as { id: string };
}

// ============================================================
// Batch helpers (Supabase doesn't have a Firestore-style batch,
// but we can run multiple upserts/updates in a single RPC or
// use Promise.all for independent writes)
// ============================================================

/** Batch update multiple rows in the same table */
async function batchUpdate(
  table: string,
  updates: Array<{ id: string; data: Record<string, unknown> }>
): Promise<void> {
  await Promise.all(updates.map(({ id, data }) => updateDoc(table, id, data)));
}

/** Batch delete multiple rows in the same table */
async function batchDelete(table: string, ids: string[]): Promise<void> {
  const { error } = await requireClient().from(table).delete().in('id', ids);
  if (error) throw error;
}

